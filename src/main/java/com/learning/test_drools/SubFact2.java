package com.learning.test_drools;

public class SubFact2 {

    private int val;
    private String name;

    public SubFact2() {}
    public SubFact2(int val, String name) {
        this.val = val;
        this.name = name;
    }

    public int getVal() {
        return val;
    }

    public void setVal(int val) {
        this.val = val;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
